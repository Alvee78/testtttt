package class19.E5;

import java.awt.Rectangle;

public class HeightAndWidthOfRectangle {
    public static void main(String[] args) {
        Rectangle rectangle1 = new Rectangle(2,21);
        Rectangle rectangle2 = new Rectangle(10,11);
        System.out.println("height = "+rectangle1.getHeight()+" width = "+rectangle1.getWidth());
        System.out.println("height = "+rectangle2.getHeight()+" width = "+rectangle2.getWidth());
    }
}
